#include <iostream>
using namespace std;

void main()
{
	setlocale(LC_ALL, "");
	const int N = 100;
	int A, m, * pm, number, array[N], i = 0, n = 0;	//Ввод данных
	cout << "Введите число А = "; cin >> A;			//Запрашиваем ввод числа А
	cout << "Введите число m = "; cin >> m;			//Запрашиваем ввод чила m
	pm = &m;										//Добовляем указатель на адрес переменной m
	while (A > 0) {									//Выполнять цикл пока число А больше 0
		number = A % 10;							//Выделяем послению цифру числа А
		if (number % *pm == 0 && number != 0) {		//Если цифра кратно переменной на которую указывает *pm и неравна 0, то
			array[i] = number;						//Добовляем эту цифру в массив
			n += 1;									
			i++;
		}
		A = A / 10;									//Убираем последнию цифру числа А
	}
	cout << "Цифры кратные " << *pm << " : ";		//Выводим все числа массива
	for (i = 0; i < n; i++) {
		cout << array[i];
	}
}